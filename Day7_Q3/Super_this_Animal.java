package com.Day7_Q3;

public class Super_this_Animal {

	int num;
	
	public Super_this_Animal(int num) {
		this.num = num;
	}
}


/*
 * use of this keyword:-
 * 
 * this is a reserved keyword.
 * using this keyword in java we refers to the current object. Using this keyword we can
 * assign the values of the variables of the current object.
 * 
 * 
 * 
 * use of super keyword:-
 * 
 * using super keyword in java we refers to the immediate parent class. 
 * Every constructor have a super keyword in 1st line which is hidden and understood by everyone.
 * 
 * when any class doesn't have any parent class then it's parent class is Object class
 * and super refers to the Object class and therefore we are able to use methods of
 * Object class.
 */